    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        String vin2 = vin.getText();
        String colorv = color.getText();
        int autonomiav = Integer.parseInt(autonomiaBateria.getText());
        String materialBat = materialBateria.getText();
        double tiempoBateria = (int) tiempoEnsamblajeBateria.getValue();
        //=====================================================
        bateria bateriaNueva = new bateria(autonomiav, materialBat, tiempoBateria);
        String tipoCarroceriac = this.tipoCarroceria.getText();
        int capacidaMale = Integer.parseInt(capcidadMaletero.getText());
        double tiempoCarroceriaC = (int) this.tiempoCarroceria.getValue();
        carroceria nuevaCarroceria = new carroceria(tipoCarroceriac, capacidaMale, tiempoCarroceriaC);
        //===========================================
        String materialInte = materialInterior.getText();
        String tipoV = tipoVolante.getText();
        int numerobot = Integer.parseInt(cantidadBotones.getText());
        double tiempoInterior = (int) this.tiempoEnsamblajeInterior.getValue();
        interior interiornuevo = new interior(materialInte, tipoV, numerobot, tiempoInterior);
        //=====================================================================
        String tieneVisor = visor.getText();
        int numeroPantallas = Integer.parseInt(cantidadPantallas.getText());
        int ancho = Integer.parseInt(anchoPantallaPrincipal.getText());
        double tiemposistema = (int) this.tiempoEnsamblajeSistema.getValue();
        sistemaInfro sistema = new sistemaInfro(numeroPantallas, tieneVisor, numeroPantallas, tiemposistema);
        //=================================================
        String tieneMasa = masaje.getText();
        String elecOManual = electricoManual.getText();
        String material = MaterialAsientos.getText();

        double tiempoA = (int) this.tiempoEnsamblajeAsientos.getValue();
        asientos asientos = new asientos(material, elecOManual, tieneMasa, tiempoA);
        //==========================
        vehiculo vehiculoN = new vehiculo(vin2, colorv, bateriaNueva, nuevaCarroceria, interiornuevo, sistema, asientos);
        vehiculos.add(vehiculoN);
        
        
        adminVehiculos admin =new adminVehiculos("./vehiculos.txt");
        admin.cargarArchivo();
        admin.setVehiculo(vehiculoN);
        System.out.println(admin.getListaVehiculos().size());
        for (vehiculo object : admin.getListaVehiculos()) {
            System.out.println(object.getVin());
        }
        //System.out.println(admin.getListaPersonas());
        try {
            admin.escribirArchivo();
            // TODO add your handling code here:
            JOptionPane.showMessageDialog(agregarVehicu, "vehiculo guardado exitosamente");
        } catch (IOException ex) {
            Logger.getLogger(examen2Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
         

    }//GEN-LAST:event_jButton3MouseClicked

