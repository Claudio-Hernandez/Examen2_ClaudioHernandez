    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        try {
            FileWriter fw = null;
            BufferedWriter bw = null;
            File archivo = new File("./vehiculos.txt");
            try {
                fw = new FileWriter(archivo, true);
                bw = new BufferedWriter(fw);
                for (vehiculo t : vehiculos) {
                    bw.write(t.toString() + ";");

                }
                bw.flush();
            } catch (Exception ex) {
            }
            bw.close();
            fw.close();
            FileWriter fw2 = null;
            BufferedWriter bw2 = null;
            File archivo2 = new File("./ensambladores.txt");
            try {
                fw2 = new FileWriter(archivo2, true);
                bw2 = new BufferedWriter(fw2);
                for (ensamblador t : ensambladores) {
                    bw2.write(t.toString() + ";");

                }
                bw2.flush();
            } catch (Exception ex) {
            }
            bw2.close();
            fw2.close();
            // TODO add your handling code here:
            JOptionPane.showMessageDialog(this, "guardado exitoso, puede revisar los archivos de texto");
            System.exit(0);
        } catch (IOException ex) {
            Logger.getLogger(examen2Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed

